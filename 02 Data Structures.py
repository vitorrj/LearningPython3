### LISTS
mylist = [24, "apple", 1984.0, "vitor"]
print(mylist[-1])           # "vitor" the last element of the list
print(len(mylist))          # 2 elements

### TUPLES
mytupple = (1,3,4)

print(mytupple)
##### Slicing and concatenation
print(mylist[1:3])          # "apple" + 1984.0

##### Repeating a list
repeatedlist = [0] * 4      # [0, 0, 0, 0]
print(repeatedlist)

